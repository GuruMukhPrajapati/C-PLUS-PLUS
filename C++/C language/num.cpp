# include<iostream>
using namespace std;

int main()
{
    int num1, num2;
    cout<<"Enter the value of num1:\n"; /*we are callled Insertion operator '<<' */
    cin>>num1; /*we  are called extraction operator '>>'*/

    cout<<"Enter the value of num2:\n"; /*we are callled Insertion operator '<<' */
    cin>>num2; /*we  are called extraction operator '>>'**/
    cout<<"The sum is "<< num1+num2;
    return 0;

}
